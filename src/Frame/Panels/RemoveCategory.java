    private void setPanel(Category c , Apparel a)
    {
        jTextField1.setText(""+a.CountCategoryinApparel(c.getCategoryName()));
        setTable(a,c);
        //setTable(a);
    }
    private void resetPanel()
    {
        jTextField1.setText("");
        resetTable();
        jPanelWrongSearchKey.setVisible(false);
    }
    private void setTable( Apparel a,Category c)
    {
        resetTable();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object rowData[]= new Object[5];
        ArrayList<Apparel> x = a.SpecificCategory(c.getCategoryName());
        //ArrayList<Apparel> x = a.SpecificCategory(jTextFieldSearchKey.getText());
        for(Apparel app : x){
            rowData[0]=app.getNameofCategory();
            rowData[1]=app.getStockID();
            rowData[2]=app.getType();
            rowData[3]=app.getPrice();
            rowData[4]=app.getQuantity();
            model.addRow(rowData);
        }
    }
    private void resetTable()
     {
         try{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            while(jTable1.getRowCount()>0){
                for(int i=0;i<jTable1.getRowCount();i++)
                    model.removeRow(i);
            }
            model.removeRow(0);
        }
        catch(Exception e)
        {
            ;
        }
     }

