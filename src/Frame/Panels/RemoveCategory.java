    private void jLabel28MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel28MouseClicked
        // TODO add your handling code here:
        resetPanel();
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Category a = new Category();
                a=a.getCategory(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(a!=null){
                    Apparel app = new Apparel();
                    if(app.searchCaterogyInApparel(a.getCategoryName())){
                        setPanel(a,app);
                        jLabelSuccessOrFail.setText("Founded Category :)");
                    }
                    else{
                        jTextField1.setText("0");
                        jLabelSuccessOrFail.setText("Not Found Clothes have this Category :(");
                        JOptionPane.showMessageDialog(null, "Not Found Clothes have this Category Try Again :(");
                    }
                }

                else{
                    jLabelSuccessOrFail.setText("Not Found :(");
                    JOptionPane.showMessageDialog(null, "Not Found Try Again :(");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                jPanelWrongSearchKey.setVisible(true);
            }

        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Apparel ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel28MouseClicked

