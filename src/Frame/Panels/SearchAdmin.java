    private void jLabel32MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel32MouseClicked
        // TODO add your handling code here:
        
        jPanelWrongEmail.setVisible(false);
        jPanelWrongID.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongNotAllowed.setVisible(false);
        jPanelWrongPass.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongUserName.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Admin a = new Admin();
                if(a.removeAdmin(Integer.parseInt(jTextFieldSearchKey.getText()))){
                    resetPanel();
                    jLabelSuccessOrFail.setText("Removed Successfully ...! :)");
                }else{
                        jLabelSuccessOrFail.setText("Failed Removed ...! :(");
                        JOptionPane.showMessageDialog(null, "Not Found ID Admin ,Failed Update...!\n Try Again :(");
                        resetPanel();
                    }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Admin Try Again :(\nHint: ID Admin Length should more than 4 .. ;)");
                jPanelWrongSearchKey.setVisible(true);
            }
        }
        else
            jLabelSuccessOrFail.setText("Missing Required ID Admin ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel32MouseClicked

