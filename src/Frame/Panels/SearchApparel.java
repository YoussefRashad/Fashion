    private void setPanel(Apparel a)
    {
        jTextFieldCategoryName.setText(""+a.getNameofCategory());
        jTextFieldLQuantity.setText(""+a.getQuantity());
        jTextFieldPrice.setText(""+a.getPrice());
        jTextFieldStockID.setText(""+a.getStockID());
        jTextFieldType.setText(""+a.getType());
        ArrayList<String> Current = new Category().CurrentlyCategory();
            for(int i=0;i<Current.size();i++)
                jComboBoxCurrentlyCategory.addItem(Current.get(i));
            jComboBoxCurrentlyCategory.setSelectedItem(jTextFieldCategoryName.getText());
        
    }
