    private void jLabel28MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel28MouseClicked
        // TODO add your handling code here:
        resetPanel();
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Apparel a = new Apparel();
                a=a.getApparel(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(a!=null){
                    setPanel(a);
                    jLabelSuccessOrFail.setText("Founded Apparel :)");
                    jTextFieldQuantity.setEditable(true);

                }
                else{
                    jLabelSuccessOrFail.setText("Not Found :(");
                    JOptionPane.showMessageDialog(null, "Not Found Try Again :(");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid Stock ID Try Again :(\nHint: Stock ID Length should more than 4 .. ;)");
                jPanelWrongSearchKey.setVisible(true);
            }

        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Apparel ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel28MouseClicked

