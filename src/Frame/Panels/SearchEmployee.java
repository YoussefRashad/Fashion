    private void setPanel(Employee a)
    {
        jTextFieldEmail.setText(""+a.getEmail());
        jTextFieldID.setText(""+a.getID());
        jTextFieldName.setText(""+a.getName());
        jTextFieldPhoneNo.setText(""+a.getPhoneNo());
        jTextFieldPass.setText(""+a.getPassword());
        jTextFieldUserName.setText(""+a.getUserName());
        jTextFieldQualification.setText(""+a.getQualification());
        jComboBoxGender.setSelectedItem(a.getGender());
        String sepreted1[]=a.getDateOfJoin().split("#");
        jComboBoxdayJoin.setSelectedItem(sepreted1[2]);
        jComboBoxmonthJoin.setSelectedItem(sepreted1[1]);
        jComboBoxyearJoin.setSelectedItem(sepreted1[0]);
        String sepreted2[]=a.getDateOfBirth().split("#");
        jComboBoxdayBirth.setSelectedItem(sepreted2[2]);
        jComboBoxmonthBirth.setSelectedItem(sepreted2[1]);
        jComboBoxyearBirth.setSelectedItem(sepreted2[0]);
    }
