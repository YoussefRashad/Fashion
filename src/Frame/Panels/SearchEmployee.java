    private void jLabel28MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel28MouseClicked
        // TODO add your handling code here:
        resetPanel();
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Employee a = new Employee();
                a=a.getEmployee(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(a!=null){
                    setPanel(a);
                    jLabelSuccessOrFail.setText("Founded Employee :)");
                }
                else{
                    jLabelSuccessOrFail.setText("Not Found :(");
                    JOptionPane.showMessageDialog(null, "Not Found Try Again :(");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                jPanelWrongID.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Employee ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel28MouseClicked

