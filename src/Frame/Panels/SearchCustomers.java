    private void resetPanel()
    {
        jTextFieldEmail.setText("");
        jTextFieldName.setText("");
        jTextFieldPhoneNo.setText("");
        
        jPanelWrongEmail.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongNotAllowed.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
    }
    private void jLabel32MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel32MouseClicked
        // TODO add your handling code here:
        
        jPanelWrongEmail.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongNotAllowed.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkName(jTextFieldSearchKey.getText())){
                Customer a = new Customer();
                if(a.removeCustomer(jTextFieldSearchKey.getText())){
                    resetPanel();
                    jLabelSuccessOrFail.setText("Removed Successfully ...! :)");
                }else{
                    jLabelSuccessOrFail.setText("Failed Removed ...! :(");
                    JOptionPane.showMessageDialog(null, "Not Found Name of Customer ,Failed Update...!\n Try Again :(");
                    resetPanel();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid Name of Customer Try Again :(");
                jPanelWrongSearchKey.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required Name of Customer ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel32MouseClicked

