    private void jLabel31MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel31MouseClicked
        // TODO add your handling code here:
        jPanelWrongEmail.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongNotAllowed.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        
        if(!jTextFieldSearchKey.getText().equals("")&&!jTextFieldEmail.getText().equals("")&&!jTextFieldName.getText().equals("")&&!jTextFieldPhoneNo.getText().equals(""))
        {
            Validation v = new Validation();
            if(v.checkName(jTextFieldSearchKey.getText())){
                if(v.checkName(jTextFieldName.getText())){
                    if(v.checkEmail(jTextFieldEmail.getText())){
                        if(v.checkPhoneNo(jTextFieldPhoneNo.getText())){
                            Customer a = new Customer();
                            if(!jTextFieldName.getText().equals(jTextFieldSearchKey.getText())){
                                if(!a.Dublicate(jTextFieldName.getText())){
                                    a.setName(jTextFieldName.getText());
                                    a.setEmail(jTextFieldEmail.getText());
                                    a.setPhoneNo(jTextFieldPhoneNo.getText());
                                    if(a.editInCustomer((jTextFieldSearchKey.getText()))){
                                        jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                        resetPanel();
                                    }
                                    else{
                                        jLabelSuccessOrFail.setText("Failed Updated ...! :(");
                                        JOptionPane.showMessageDialog(null, "Not Found Name of Customer ,Failed Update...!\n Try Again :(");
                                        resetPanel();
                                    }
                                }
                                else{
                                    jLabelSuccessOrFail.setText("Not Allowed New Name Customer :(");
                                    jPanelWrongNotAllowed.setVisible(true);
                                    JOptionPane.showMessageDialog(null, "Not Allowed Name Customer , Name Customer is Reserved...!\n Try Again :(");
                                 }
                            }
                            else{
                                a.setName(jTextFieldName.getText());
                                a.setEmail(jTextFieldEmail.getText());
                                a.setPhoneNo(jTextFieldPhoneNo.getText());
                                if(a.editInCustomer((jTextFieldSearchKey.getText()))){
                                    jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                    resetPanel();
                                }
                                else{
                                    jLabelSuccessOrFail.setText("Failed Updated ...! :(");
                                    JOptionPane.showMessageDialog(null, "Not Found Name of Customer ,Failed Update...!\n Try Again :(");
                                    resetPanel();
                                }
                            }
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                            jPanelWrongPhoneNo.setVisible(true);
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Not valid Input for Email Try Again :(");
                        jPanelWrongEmail.setVisible(true);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                    jPanelWrongName.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                jPanelWrongSearchKey.setVisible(true);
            }
        }else
        jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
    }//GEN-LAST:event_jLabel31MouseClicked

